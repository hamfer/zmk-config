// ===== RENDER =====
static void bongo_cat_render(lv_obj_t *scr) {
#ifdef IS_LEFT
    const lv_img_dsc_t *img_src;

    // Toggle paws every 300ms if WPM > 0
    if (current_wpm > 0 && (k_uptime_get_32() - last_toggle) > 300) {
        paw_up = !paw_up;
        last_toggle = k_uptime_get_32();
    }

    img_src = paw_up ? &bongo_all_up_img : &bongo_left_down_img;

    // Draw cat
    lv_obj_t *img = lv_img_create(scr);
    lv_img_set_src(img, img_src);
    lv_obj_align(img, LV_ALIGN_TOP_MID, 0, 0);

    /* Show WPM under it */
    wpm_label = lv_label_create(scr);
    lv_label_set_text(wpm_label, "WPM: 0");
    lv_obj_align(wpm_label, LV_ALIGN_BOTTOM_MID, 0, -2);

    zmk_display_show(scr);
#endif
}

int bongo_wpm_listener(const zmk_event_t *eh) {
    char buf[16];
    snprintf(buf, sizeof(buf), "WPM: %d", zmk_wpm_get_state());
    lv_label_set_text(wpm_label, buf);
    return 0;
}

ZMK_LISTENER(bongo_wpm_listener, bongo_wpm_listener);
