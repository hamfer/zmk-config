// ===== STATE =====
static uint32_t last_toggle = 0;
static bool paw_up = true;

// ===== RENDER =====
static void bongo_cat_render(lv_obj_t *scr) {
#ifdef IS_LEFT
    const lv_img_dsc_t *img_src;

    // Toggle paws every 300ms if WPM > 0
    if (current_wpm > 0 && (k_uptime_get_32() - last_toggle) > 300) {
        paw_up = !paw_up;
        last_toggle = k_uptime_get_32();
    }

    img_src = paw_up ? &bongo_all_up_img : &bongo_left_down_img;

    // Draw cat
    lv_obj_t *img = lv_img_create(scr);
    lv_img_set_src(img, img_src);
    lv_obj_align(img, LV_ALIGN_TOP_MID, 0, 0);
#endif
}

ZMK_DISPLAY_WIDGET(bongo_cat_widget, bongo_cat_render);

