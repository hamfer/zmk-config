// ===== RENDER =====
static void bongo_cat_render(lv_obj_t *scr) {
#ifdef IS_LEFT
    const lv_img_dsc_t *img_src;

    // Toggle paws every 300ms if WPM > 0
    if (current_wpm > 0 && (k_uptime_get_32() - last_toggle) > 300) {
        paw_up = !paw_up;
        last_toggle = k_uptime_get_32();
    }

    img_src = paw_up ? &frame1_img : &frame2_img;

    // Draw cat
    lv_obj_t *img = lv_img_create(scr);
    lv_img_set_src(img, img_src);
    lv_obj_align(img, LV_ALIGN_TOP_MID, 0, 0);

    // Draw WPM
    char buf[16];
    snprintf(buf, sizeof(buf), "WPM:%d", current_wpm);
    lv_obj_t *label = lv_label_create(scr);
    lv_label_set_text(label, buf);
    lv_obj_align(label, LV_ALIGN_BOTTOM_MID, 0, 0);
#endif
}

// ===== LISTENER =====
static int wpm_listener(const zmk_event_t *eh) {
    const struct wpm_state_changed *ev = as_wpm_state_changed(eh);
    if (!ev) return 0;
    current_wpm = ev->state;
    return 0;
}

